import { writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { SITE_URL, seoLandingUrls } from './seo-data.mjs';

function today() {
  const parts = new Intl.DateTimeFormat('en', {
    timeZone: 'Asia/Ho_Chi_Minh',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
  }).formatToParts(new Date());
  const get = (type) => parts.find((part) => part.type === type)?.value || '';
  return `${get('year')}-${get('month')}-${get('day')}`;
}

function escapeXml(value) {
  return value
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&apos;');
}

function toUrlEntry(item) {
  const loc = `${SITE_URL}${item.path}`;
  return `  <url>
    <loc>${escapeXml(loc)}</loc>
    <lastmod>${today()}</lastmod>
    <changefreq>${item.changefreq}</changefreq>
    <priority>${item.priority}</priority>
  </url>`;
}

const seen = new Set();
const duplicates = seoLandingUrls.filter((item) => {
  if (seen.has(item.path)) return true;
  seen.add(item.path);
  return false;
});

if (duplicates.length > 0) {
  throw new Error(`Duplicate SEO landing paths: ${duplicates.map((item) => item.path).join(', ')}`);
}

const xml = `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by scripts/generate-seo-sitemap.mjs. Do not edit manually. -->
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${seoLandingUrls.map(toUrlEntry).join('\n')}
</urlset>
`;

await writeFile(resolve('public/sitemap-seo-landing.xml'), xml, 'utf8');
console.log(`Generated public/sitemap-seo-landing.xml with ${seoLandingUrls.length} URLs.`);
