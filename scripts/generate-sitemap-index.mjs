import { writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { SITE_URL } from './seo-data.mjs';

function today() {
  const parts = new Intl.DateTimeFormat('en', {
    timeZone: 'Asia/Ho_Chi_Minh',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
  }).formatToParts(new Date());
  const get = (type) => parts.find((part) => part.type === type)?.value || '';
  return `${get('year')}-${get('month')}-${get('day')}`;
}

const sitemapFiles = [
  'sitemap-static.xml',
  'sitemap-seo-landing.xml',
  'sitemap-movies-recent.xml',
  'sitemap-movies-upcoming.xml',
  ...Array.from({ length: 8 }, (_, index) => `sitemap-movies-${index + 1}.xml`),
];

const date = today();
const xml = `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by scripts/generate-sitemap-index.mjs. Last updated: ${date} -->
<sitemapindex xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${sitemapFiles.map((file) => `  <sitemap>
    <loc>${SITE_URL}/${file}</loc>
    <lastmod>${date}</lastmod>
  </sitemap>`).join('\n')}
</sitemapindex>
`;

await writeFile(resolve('public/sitemap.xml'), xml, 'utf8');
console.log(`Generated public/sitemap.xml with ${sitemapFiles.length} child sitemaps.`);

const movieSitemapFiles = sitemapFiles.filter((file) => file.startsWith('sitemap-movies-'));
const movieXml = `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by scripts/generate-sitemap-index.mjs. Last updated: ${date} -->
<sitemapindex xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${movieSitemapFiles.map((file) => `  <sitemap>
    <loc>${SITE_URL}/${file}</loc>
    <lastmod>${date}</lastmod>
  </sitemap>`).join('\n')}
</sitemapindex>
`;

await writeFile(resolve('public/sitemap-movies.xml'), movieXml, 'utf8');
console.log(`Generated public/sitemap-movies.xml with ${movieSitemapFiles.length} child sitemaps.`);
